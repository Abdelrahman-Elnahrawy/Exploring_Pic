#ifndef DIO_TYPES_H
#define	DIO_TYPES_H

// Pin state macros
#define DIO_PIN_LOW      0  // Logic Low
#define DIO_PIN_HIGH     1  // Logic High

// Pin direction macros
#define DIO_DIR_INPUT    0  // Input direction
#define DIO_DIR_OUTPUT   1  // Output direction

// Pull-up type macros
#define DIO_PULLUP_DISABLE          0  // No pull-up or pull-down
#define DIO_PULLUP_ENABLE           1  // Enable pull-up resistor

// Pin number macros
#define DIO_PIN_0        0  // Pin 0
#define DIO_PIN_1        1  // Pin 1
#define DIO_PIN_2        2  // Pin 2
#define DIO_PIN_3        3  // Pin 3
#define DIO_PIN_4        4  // Pin 4
#define DIO_PIN_5        5  // Pin 5


#endif	/* DIO_TYPES_H */

